package conditionals;

public class IfElseRunner {

	public static void main(String[] args) {

		puzzel();
	}

	private static void puzzel() {
		int k = 20;
		if (k <= 20) {
			System.out.println("1");
		} else if (k >= 20) {
			System.out.println("2");
		} else {
			System.out.println("3");
		}

	}

}
